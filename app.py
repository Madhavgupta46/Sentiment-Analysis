import streamlit as st
import pickle

# Load trained model and vectorizer
model = pickle.load(open("model.pkl", "rb"))
vectorizer = pickle.load(open("vectorizer.pkl", "rb"))

st.set_page_config(page_title="Sentiment Analysis", page_icon="ðŸ§ ")

st.title("ðŸ§  Sentiment Analysis App")
st.write("Predict sentiment: Negative | Neutral | Positive")

text = st.text_area("Enter text here")

if st.button("Predict Sentiment"):
    if text.strip() == "":
        st.warning("Please enter some text")
    else:
        text_vector = vectorizer.transform([text])
        prediction = model.predict(text_vector)[0]

        if prediction == 0:
            st.error("ðŸ”´ Negative Sentiment")
        elif prediction == 1:
            st.info("ðŸŸ¡ Neutral Sentiment")
        elif prediction == 2:
            st.success("ðŸŸ¢ Positive Sentiment")